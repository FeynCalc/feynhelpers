LToolsA0i[id_String, m_]:=
	Block[{idVal},

		If[	MemberQ[idList, id],
			idVal = ToExpression["LoopTools`" <> id],
			Message[LTools::failmsg,"Unknown id " <> id];
			Abort[]
		];

		LoopTools`A0i[idVal,m]
	]/; FeynCalc`Package`ltLoaded;

