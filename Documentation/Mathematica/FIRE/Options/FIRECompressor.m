(*FIRECompressor*)


(* ::Text:: *)
(*`FIRECompressor` is an option for `FIRECreateConfigFile` and other functions of the FIRE interface.*)


(* ::Text:: *)
(*It specifies the `#compressor` parameter to be set in a FIRE .config-file. The default value is `"zstd"`.*)


